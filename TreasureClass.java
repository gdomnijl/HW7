/** 
	 * The class TreasureClass holds three sub-fields:
	 * item1: a string of item
	 * item2: a string of item
	 * item3: a string of item
	 */
public class TreasureClass {
	public String item1; 
	public String item2; 
	public String item3; 

	public TreasureClass(String item1, String item2, String item3) {
		this.item1 = item1; 
		this.item2 = item2; 
		this.item3 = item3; 
	}
}
